def call_signal_analysis(call_data: dict) -> dict:
    tc   = call_data['total_calls_made']
    uc   = call_data['unique_numbers_contacted']
    dur  = call_data['avg_call_duration_min']
    cph  = call_data['calls_per_hour']
    spam = call_data['spam_reports']

    call_diversity_ratio = uc / (tc + 1)
    short_call_flag      = int(dur < 1.0)
    high_freq_flag       = int(cph > 6.0)
    spam_intensity       = spam / (tc + 1)
    suspicion_score      = (
        0.30 * cph +
        0.30 * spam +
        0.20 * short_call_flag * 10 +
        0.20 * call_diversity_ratio * 10
    )

    feature_vector = [[
        tc, uc, dur, cph, spam,
        call_diversity_ratio, short_call_flag, high_freq_flag,
        spam_intensity, suspicion_score
    ]]

    scaled = scaler.transform(feature_vector)

    is_scam_pred = int(rf_clf.predict(scaled)[0])
    scam_prob    = round(float(rf_clf.predict_proba(scaled)[0][1]) * 100, 2)
    ml_risk      = round(float(np.clip(rf_reg.predict(scaled)[0], 0, 10)), 2)

    rule_risk = calculate_risk_score(tc, uc, dur, cph, spam)

    final_risk = round(0.65 * ml_risk + 0.35 * rule_risk, 2)
    final_risk = round(min(final_risk, 10.0), 2)

    if final_risk >= 7.5:
        risk_level = 'HIGH'
    elif final_risk >= 5.0:
        risk_level = 'MEDIUM-HIGH'
    elif final_risk >= 3.0:
        risk_level = 'MEDIUM'
    elif final_risk >= 1.5:
        risk_level = 'LOW-MEDIUM'
    else:
        risk_level = 'LOW'

    return {
        'source'       : 'call',
        'call_no'      : call_data.get('call_no', 'Unknown'),
        'risk_score'   : final_risk,
        'risk_level'   : risk_level,
        'is_scam_pred' : is_scam_pred,
        'scam_prob'    : scam_prob,
        'flags'        : []
    }