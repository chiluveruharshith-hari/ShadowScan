def generate_indian_numbers(n):
    numbers = []
    prefixes = ['6', '7', '8', '9']
    for _ in range(n):
        prefix = np.random.choice(prefixes)
        rest   = ''.join([str(np.random.randint(0, 10)) for _ in range(9)])
        numbers.append(prefix + rest)
    return numbers

def calculate_risk_score(total_calls, unique_numbers, avg_duration_min,
                          calls_per_hour, spam_reports):
    score = 0.0

    if calls_per_hour <= 3:
        score += 0.0
    elif calls_per_hour <= 6:
        score += 1.0
    elif calls_per_hour <= 10:
        score += 1.8
    else:
        score += 2.5

    spam_ratio = spam_reports / (total_calls + 1)
    score += min(spam_ratio * 10, 3.0)

    if avg_duration_min < 0.5:
        score += 2.0
    elif avg_duration_min < 1.5:
        score += 1.2
    elif avg_duration_min < 3.0:
        score += 0.5
    else:
        score += 0.0

    diversity = unique_numbers / (total_calls + 1)
    score += min(diversity * 2.5, 1.5)

    if total_calls > 100:
        score += 1.0
    elif total_calls > 50:
        score += 0.5
    elif total_calls > 20:
        score += 0.2

    return round(min(score, 10.0), 2)


